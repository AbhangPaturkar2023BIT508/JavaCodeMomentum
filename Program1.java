class Program1 {
    public static void main(String[] args) {
        int x = 0103; // Leading '0' before numeric literal indicates Octal Value
        System.out.println("x = " + x); // x = 67

        int y = 103;
        System.out.println("y = " + y); // y = 103

        int z = 0115;
        System.out.println("z = " + z); // z = 77
    }
}